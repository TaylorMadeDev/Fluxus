# ── Flattened completion list ──────────────────────────────────────

def get_all_completions() -> list[str]:
    """Return sorted unique list of all Minescript symbol names for autocomplete."""
    seen = set()
    result = []
    for entry in MINESCRIPT_API + MINESCRIPT_TYPES:
        name = entry.name
        if name not in seen:
            seen.add(name)
            result.append(name)
        # Also provide dotted sub-names  e.g. "BlockPack.read_world"
        if "." in name:
            short = name.split(".")[-1]
            if short not in seen:
                seen.add(short)
                result.append(short)
    result.sort()
    return result


def get_api_entry(name: str) -> APIEntry | None:
    """Lookup an API entry by name (case-insensitive)."""
    low = name.lower()
    for entry in MINESCRIPT_API + MINESCRIPT_TYPES:
        if entry.name.lower() == low:
            return entry
    return None



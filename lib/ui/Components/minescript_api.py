# ── Data classes ───────────────────────────────────────────────────

class APIEntry:
    """One documented symbol (function, class, constant)."""
    __slots__ = ("name", "module", "signature", "description", "since", "category", "params", "returns")

    def __init__(self, name, module="minescript", signature="", description="",
                 since="", category="", params=None, returns=""):
        self.name = name
        self.module = module
        self.signature = signature
        self.description = description
        self.since = since
        self.category = category
        self.params = params or []
        self.returns = returns

    @property
    def completion_text(self) -> str:
        return self.name

    @property
    def detail_text(self) -> str:
        sig = f"({self.signature})" if self.signature else ""
        header = f"{self.module}.{self.name}{sig}"
        parts = [header, "", self.description]
        if self.params:
            parts.append("\nParameters:")
            for p in self.params:
                parts.append(f"  • {p}")
        if self.returns:
            parts.append(f"\nReturns: {self.returns}")
        if self.since:
            parts.append(f"\nSince: {self.since}")
        return "\n".join(parts)


